function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
    document.getElementById("mySidenavcls").style.position = "absolute";
    document.getElementById("mySidenavcls").style.opacity = "1";
}

function closeNav() {
    document.getElementById("mySidenav").style.width = "0px";
    document.getElementById("mySidenavcls").style.position = "relative";
    document.getElementById("mySidenavcls").style.opacity = "0";


}